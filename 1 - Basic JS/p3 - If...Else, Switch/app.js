// * * * Comparision Operators * * *// 
// > maggiore
// >= maggiore o uguale
// < minore
// <= minore o uguale
// == uguale
// != non uguale

let age = 5

if (age < 3) {
  console.log('You are a Baby')
} else if ( age < 17) {
  console.log('You are a Child')
} else if ( age >= 18) {
  console.log('You are an Adult')
}

if (age == 18) {
  console.log('You have 18 year old')
}
if (age != 18) {
  console.log('You DONT have 18 year old')
}